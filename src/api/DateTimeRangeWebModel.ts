
/**
 * Represents a range of time in UTC
 * @export
 * @interface DateTimeRangeWebModel
 */
export interface DateTimeRangeWebModel {
  /**
   * starting point of the dateTime range
   * @type {string}
   * @memberof DateTimeRangeWebModel
   */
  start?: string;
  /**
   * ending point of the dateTime range
   * @type {string}
   * @memberof DateTimeRangeWebModel
   */
  end?: string;
}
